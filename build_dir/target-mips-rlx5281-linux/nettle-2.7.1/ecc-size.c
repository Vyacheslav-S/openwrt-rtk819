#if HAVE_CONFIG_H
# include "config.h"
#endif

#include "ecc.h"
#include "ecc-internal.h"

mp_size_t
ecc_size (const struct ecc_curve *ecc)
{
  return ecc->size;
}

mp_size_t
ecc_size_a (const struct ecc_curve *ecc)
{
  return 2*ecc->size;
}

mp_size_t
ecc_size_j (const struct ecc_curve *ecc)
{
  return 3*ecc->size;
}
